    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        sr.dispose();
        sr = new SearchResults();
        String staff_no = txtStaffRef.getText();
        String first_name = txtFirstName.getText();
        String surname = txtSurname.getText();
        String address = txtAddress.getText();
        String postcode = txtPostcode.getText();
        String phone_no = txtPhoneNumber.getText();
        String dob= "";
        if(comboDobDay.getSelectedIndex() == 0 
                && comboDobMonth.getSelectedIndex() == 0 
                && comboDobYear.getSelectedIndex() == 0){
            dob = "";
        }
        boolean firstTerm = true;
        String where = "";
        if(!staff_no.equalsIgnoreCase("")){
            where += "staff_no IN ('" + staff_no + "')";
            firstTerm = false;
        }
        if(!first_name.equalsIgnoreCase("")){
            if(firstTerm == true){
                where += "first_name IN ('" + first_name + "')";
                firstTerm = false;
            }else{
                where += " AND first_name IN ('" + first_name + "')";
            }
        }
        if(!surname.equalsIgnoreCase("")){
            if(firstTerm == true){
                where += "surname IN ('" + surname + "')";
                firstTerm = false;
            }else{
                where += " AND surname IN ('" + surname + "')";
            }
        }
        if(!address.equalsIgnoreCase("")){
            if(firstTerm == true){
                where += "address IN ('" + address + "')";
                firstTerm = false;
            }else{
                where += " AND address IN ('" + address + "')";
            }
        }
        if(!postcode.equalsIgnoreCase("")){
            if(firstTerm == true){
                where += "postcode IN ('" + postcode + "')";
                firstTerm = false;
            }else{
                where += " AND postcode IN ('" + postcode + "')";
            }
        }
        if(!phone_no.equalsIgnoreCase("")){
            if(firstTerm == true){
                where += "phone_no IN ('" + phone_no + "')";
            }else{
                where += " AND phone_no IN ('" + phone_no + "')";
            }
        }
        if(!dob.equalsIgnoreCase("")){
            if(firstTerm == true){
                where += "dob IN ('" + dob + "')";
            }else{
                where += " AND dob IN ('" + dob + "')";
            }
        }
        try {
            if(where.equalsIgnoreCase("")){
                searchFullTable("staff");
            }else{
                searchWhere("staff", where);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerGUI.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
        sr.setVisible(true);
    }//GEN-LAST:event_btnSearchActionPerformed

