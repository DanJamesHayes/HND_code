    private void btnAddStaffMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStaffMemberActionPerformed
        String entities = "staff_no, first_name, surname, address, postcode, "
                + "phone_no, dob";
        String staff_no = txtStaffRef.getText();
        String first_name = txtFirstName.getText();
        String surname = txtSurname.getText();
        String address = txtAddress.getText();
        String postcode = txtPostcode.getText();
        String phone_no = txtPhoneNumber.getText();
        String dob = "";
        if(comboDobDay.getSelectedIndex() == 0 
                && comboDobMonth.getSelectedIndex() == 0 
                && comboDobYear.getSelectedIndex() == 0){
            dob = "''";
        }else if(comboDobDay.getSelectedIndex() == 0 
                || comboDobMonth.getSelectedIndex() == 0 
                || comboDobYear.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Incomplete date of birth "
                    + "entered", "Input Error", 
                    JOptionPane.ERROR_MESSAGE);
        }else{
            dob = "TO_DATE ('" + comboDobDay.getSelectedItem().toString() + "-"
                    + comboDobMonth.getSelectedItem().toString() + "-" 
                    + comboDobYear.getSelectedItem().toString() + "')";
        }
        Person p1 = PersonFactory.getInstance("staff");
        p1.setRef(staff_no);
        p1.setFirst_name(first_name);
        p1.setSurname(surname);
        p1.setAddress(address);
        p1.setPostcode(postcode);
        p1.setPhone_no(phone_no);
        p1.setDob(dob);
        String values = p1.toString();
        try {
            DBmethods.insertRecord("staff", entities, values);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerGUI.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddStaffMemberActionPerformed

