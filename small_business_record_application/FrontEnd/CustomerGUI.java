    private void btnAddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCustomerActionPerformed
        String entities = "cust_no, first_name, surname, address, postcode, "
                + "phone_no";
        String cust_no = txtCustomerRef.getText();
        String first_name = txtFirstName.getText();
        String surname = txtSurname.getText();
        String address = txtAddress.getText();
        String postcode = txtPostcode.getText();
        String phone_no = txtPhoneNumber.getText();
        Person p1 = PersonFactory.getInstance("customer");
        p1.setRef(cust_no);
        p1.setFirst_name(first_name);
        p1.setSurname(surname);
        p1.setAddress(address);
        p1.setPostcode(postcode);
        p1.setPhone_no(phone_no);
        String values = p1.toString();
        try {
            DBmethods.insertRecord("customer", entities, values);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerGUI.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddCustomerActionPerformed

