    private void btnCreateWorkOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateWorkOrderActionPerformed
        String cust_no = txtCustomerRef.getText();
        String where = "cust_no IN ('" + cust_no + "')";
        String custExists = "0";
        try {
            custExists = countWhere("customer", where);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerGUI.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
        if(custExists.equalsIgnoreCase("0")){
            JOptionPane.showMessageDialog(null, "Customer reference not "
                    + "recognised", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            dispose();
            WorkOrderGUI wo = new WorkOrderGUI();
            wo.setCustRef(cust_no);
            wo.setVisible(true); 
        }
    }//GEN-LAST:event_btnCreateWorkOrderActionPerformed
    

